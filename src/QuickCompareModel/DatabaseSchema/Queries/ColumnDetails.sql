SELECT	TABLE_SCHEMA,
		TABLE_NAME,
		COLUMN_NAME,
		ORDINAL_POSITION,
		COLUMN_DEFAULT,
		IS_NULLABLE,
		DATA_TYPE,
		CHARACTER_MAXIMUM_LENGTH,
		CHARACTER_OCTET_LENGTH,
		NUMERIC_PRECISION,
		NUMERIC_PRECISION_RADIX,
		NUMERIC_SCALE,
		DATETIME_PRECISION,
		CHARACTER_SET_NAME,
		COLLATION_NAME,
		DOMAIN_SCHEMA,
		DOMAIN_NAME,
		COLUMNPROPERTY(OBJECT_ID(TABLE_SCHEMA + '.' + TABLE_NAME), COLUMN_NAME, N'IsFulltextIndexed') AS IS_FULL_TEXT_INDEXED,
		COLUMNPROPERTY(OBJECT_ID(TABLE_SCHEMA + '.' + TABLE_NAME), COLUMN_NAME, N'IsComputed') AS IS_COMPUTED,
		COLUMNPROPERTY(OBJECT_ID(TABLE_SCHEMA + '.' + TABLE_NAME), COLUMN_NAME, N'IsIdentity') AS IS_IDENTITY,
		IDENT_SEED(TABLE_SCHEMA + '.' + TABLE_NAME) AS IDENTITY_SEED,
		IDENT_INCR(TABLE_SCHEMA + '.' + TABLE_NAME) AS IDENTITY_INCREMENT,
		COLUMNPROPERTY(OBJECT_ID(TABLE_SCHEMA + '.' + TABLE_NAME), COLUMN_NAME, N'IsSparse') AS IS_SPARSE,
		COLUMNPROPERTY(OBJECT_ID(TABLE_SCHEMA + '.' + TABLE_NAME), COLUMN_NAME, N'IsColumnSet') AS IS_COLUMN_SET,
		COLUMNPROPERTY(OBJECT_ID(TABLE_SCHEMA + '.' + TABLE_NAME), COLUMN_NAME, N'IsRowGuidCol') AS IS_ROW_GUID
FROM	INFORMATION_SCHEMA.COLUMNS
WHERE	(TABLE_NAME NOT LIKE 'sys%')
AND		(TABLE_NAME NOT LIKE 'MS%')
